public class Sungjuck {
    public int b_num;
    public int b_ban;
    public float kor;
    public float eng;

    public Sungjuck(){
        this.b_num = 0000;
        this.b_ban = 0;
        this.kor = 00.00f;
        this.eng = 00.00f;
    };

    public Sungjuck(int b_num, int b_ban, float kor, float eng){
        this.b_num = b_num;
        this.b_ban = b_ban;
        this.kor = kor;
        this.eng = eng;
    }
}

